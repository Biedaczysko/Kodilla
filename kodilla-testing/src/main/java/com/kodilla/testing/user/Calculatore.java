package com.kodilla.testing.user;

public class Calculatore {
    int a;
    int b;
    public Calculatore(int a,int b)
    {
        this.a=a;
        this.b=b;
    }
    public int add()
    {
        return this.a+this.b;
    }
    public int subtract()
    {
        return this.a-this.b;
    }

}
